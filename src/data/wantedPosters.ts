// Avis de recherche (criminels traqués) — données rédigées à partir des fiches
// DofusPourLesNoobs (https://www.dofuspourlesnoobs.com/avis-de-recherche.html),
// même source que les guides de donjon. Chaque criminel est aussi un monstre
// DofusDB → la fiche relie au portrait + à /monstres/:id via resolveMonsterIdByName.
//
// Référence d'exhaustivité : l'API DofusDB liste 82 quêtes « On recherche … » (le set
// complet du jeu). Les 82 sont couverts ici. « Sans Visage » (criminel d'alignement)
// est sourcé via JeuxOnLine + DofusDB (absent de DPLN au format habituel).
//
// ⚠️ Aucune donnée inventée : niveau, zone, faiblesse, PV, mécaniques et récompenses
// proviennent toutes de la fiche source du criminel.

export type WantedElement = "feu" | "eau" | "terre" | "air" | "neutre";

// Régions de l'onglet « Avis de recherche » du jeu (milices). Sert au filtre + au tri.
export type WantedRegion =
  | "Astrub"
  | "Amakna"
  | "Bonta & Brâkmar"
  | "Saharach"
  | "Sufokia & Abysses"
  | "Frigost"
  | "Dimensions Divines";

// Une récompense = un type de jeton (Doplon / Aviton / Aliton / Kama de Glace…) et sa quantité.
export interface WantedReward {
  token: string;
  amount: number;
}

// Un sort / une mécanique du criminel (libellé + effet décrit mot pour mot par la fiche).
export interface WantedSpell {
  name: string;
  effect: string;
}

export interface WantedPoster {
  slug: string; // route /avis-de-recherche/:slug (kebab ASCII, stable)
  name: string;
  level: number; // niveau requis (sert au tri et à la couleur de pastille)
  levelLabel?: string; // libellé alternatif quand l'accès n'est pas un niveau (ex. « Ordre 1+ »)
  hp?: number; // points de vie du criminel
  region: WantedRegion;
  location: string; // zone exacte où il apparaît
  access?: string; // comment accéder / où prendre l'avis (milice, coordonnées)
  prerequisite?: string; // quête / condition préalable
  weakness: WantedElement[]; // élément(s) de faiblesse
  resists?: string; // résistances élevées / dangers à connaître
  protections?: string[]; // mécaniques de protection (invulnérabilité conditionnelle…)
  spells?: WantedSpell[]; // sorts / mécaniques de combat
  strategy: string[]; // paragraphes de stratégie (rédigés)
  rewards: WantedReward[];
  monsterName?: string; // nom DofusDB si différent du nom affiché (pour le portrait/lien)
}

